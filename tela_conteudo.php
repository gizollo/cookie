Ó bela dama, porque me abandonastes ? <br>
Deixaras seu único contemplador perdido <br>
Nunca mais pude lhe ver novamente <br>
Suas marcas me deixam cicatrizes em meu peito <br> 
Mas nunca me esquecera de seus encantos <br>
Nunca soubeste seu nome <br>
Mas para mim sempre serás minha beladama do lago.</p>
