Seu corpo esguio, à luz prateada da noite <br> 
Seu olhar nas páginas do livro, mergulhava com entusiasmo <br>
Nas águas escuras do lago, o reflexo brilhava <br>
Refletia uma certa pureza, cabelos castanhos, como rios em correnteza <br>
Minha surpresa não foi sua feição plena e bela <br>
Foi seu fascínio durante sua leitura <br>
Ela segurava um livro com mãos graciosas <br>
Páginas cheias de histórias misteriosas <br>
Aproximei-me e indaguei se poderias acender meu tabaco <br>
E com apenas um gesto simples, sorriu e acenou <br>
Ó bela dama meu coração foi tomado por ti <br>
O luar acariciava seu rosto sereno <br>
Sua face, um retrato de graça e encanto <br>
Vislumbrei seus castanhos olhos como de mel <br>
O céu estrelado e uma paisagem deslumbrante <br>
E tu segurastes um livro mais robusto que toras <br>
Mais volumoso que o lago a vossa volta <br>
Quando quase acabastes meu tabaco <br>
Ouvira tua voz a cada palavra que lia eras uma canção <br> 
Que ecoava no meus ouvidos e pensamentos <br>
Era como um raio luminoso no breu <br>
Falou de linhas da Historia, fiquei preso e hipnotizado. <br>
E minha bela dama à de partir para seu recanto <br>
Me deixando deveras em pranto e espanto <br>
Voltara na outra noite e continuaste sua leitura <br> 
